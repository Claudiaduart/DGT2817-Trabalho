# variável tempoExperiencia 
tempoExperiencia = 5

# Verifique se o valor da variável tempoExperiencia é menor que 2
if tempoExperiencia < 2:
    print("Nível de conhecimento júnior.")
# Verifique se o valor da variável tempoExperiencia é maior que 2 e menor que 5
elif 2 <= tempoExperiencia < 5:
    print("Nível de conhecimento pleno.")
# Caso contrário, imprima o texto ‘Nível de conhecimento sênior.’
else:
    print("Nível de conhecimento sênior.")