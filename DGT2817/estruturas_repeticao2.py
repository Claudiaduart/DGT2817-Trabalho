# Crie uma variável chamada texto e atribua a ela o valor 'Olá, laço for.'
texto = 'Olá, laço for.'

# Itere sobre cada caractere do texto
for item in texto:
    print(f'Caractere: {item}')

# Itere sobre um intervalo numérico de 1 a 10
for numero in range(1, 11):
    print(f'Número do intervalo: {numero}')